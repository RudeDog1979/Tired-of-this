def load_keys() -> list[str]:
    keys = [line.rstrip("\n") for line in KEYS_FILE.read_text(encoding="utf-8").splitlines() if line.strip()]
    return [k for k in keys if "\\(" not in k]


def main() -> None:
    core: dict[str, dict[str, str]] = {}
    if CORE_FILE.is_file():
        core = json.loads(CORE_FILE.read_text(encoding="utf-8"))

    merged = {**T, **core}  # core overrides for consistency
    keys = load_keys()
    out: dict[str, dict[str, str]] = {}
    missing: list[str] = []
    for key in keys:
        if key in merged:
            out[key] = merged[key]
        else:
            missing.append(key)

    if missing:
        raise SystemExit(f"Missing {len(missing)} translations:\n" + "\n".join(missing[:20]))

    OUT_FILE.parent.mkdir(parents=True, exist_ok=True)
    OUT_FILE.write_text(json.dumps(out, indent=2, ensure_ascii=False) + "\n", encoding="utf-8")
    print(f"Wrote {len(out)} entries to {OUT_FILE}")


if __name__ == "__main__":
    main()
